    and tc.table_name = 'formula_dish_links'
    and tc.constraint_type = 'FOREIGN KEY'
    and kcu.column_name = 'dish_id'
    and ccu.table_schema = 'public'
    and ccu.table_name = 'dishes'
  limit 1;

  if existing_fk is not null then
    execute format('alter table public.formula_dish_links drop constraint %I', existing_fk);
