  limit 1;

  if existing_fk is not null then
    execute format('alter table public.formula_dish_links drop constraint %I', existing_fk);
  end if;

  execute '
    alter table public.formula_dish_links
    add constraint formula_dish_links_formula_fkey
    foreign key (formula_dish_id)
    references public.dishes(id)
    on delete cascade
